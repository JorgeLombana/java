public class Variables {
  public static void main(String[] args) {
    boolean isTrue;
    char myChar;
    String myString;
    int myInteger;
    long longVariable;
    float floatVariable;

    isTrue = false;
    myChar = 'B';
    myString = "A text";
    myInteger = 222;
    longVariable = 1223131231123213211l;
    floatVariable = 98123.218312f;

    System.out.println(
        isTrue + "\n" + myString + "\n" + myChar + "\n" + myInteger + longVariable + "\n" + floatVariable);
  }
}